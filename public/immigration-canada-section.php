    // Initialisation du slider
    document.addEventListener('DOMContentLoaded', function() {
        const slider = document.querySelector('.immigration-slider');
        const slides = document.querySelectorAll('.immigration-slide');
        const prevBtn = document.querySelector('.prev-control');
        const nextBtn = document.querySelector('.next-control');
        const pagination = document.querySelector('.slider-pagination');
        
        let currentSlide = 0;
        const slidesPerView = getSlidesPerView();

        // Créer la pagination
        slides.forEach((_, index) => {
            const dot = document.createElement('div');
            dot.className = 'pagination-dot';
            if (index === 0) dot.classList.add('active');
            dot.addEventListener('click', () => goToSlide(index));
            pagination.appendChild(dot);
        });

        // Fonction pour déterminer le nombre de slides visibles
        function getSlidesPerView() {
            if (window.innerWidth < 768) return 1;
            if (window.innerWidth < 1200) return 2;
            return 3;
        }

        // Fonction pour aller à un slide spécifique
        function goToSlide(slideIndex) {
            currentSlide = slideIndex;
            const slideWidth = slides[0].offsetWidth + 30; // 30px pour le gap
            slider.scrollTo({
                left: slideIndex * slideWidth,
                behavior: 'smooth'
            });
            updatePagination();
        }

        // Fonction pour mettre à jour la pagination
        function updatePagination() {
            document.querySelectorAll('.pagination-dot').forEach((dot, index) => {
                dot.classList.toggle('active', index === currentSlide);
            });
        }

        // Événements pour les boutons de navigation
        prevBtn.addEventListener('click', () => {
            if (currentSlide > 0) {
                goToSlide(currentSlide - 1);
            }
        });

        nextBtn.addEventListener('click', () => {
            if (currentSlide < slides.length - slidesPerView) {
                goToSlide(currentSlide + 1);
            }
        });

        // Mise à jour responsive
        window.addEventListener('resize', () => {
            goToSlide(0);
        });

        // Ajouter les écouteurs d'événements pour les slides
        document.querySelectorAll('.immigration-slide').forEach(slide => {
            slide.addEventListener('click', function() {
                const program = this.getAttribute('data-program');
                openImmigrationModal(program);
            });
        });

        // Navigation au clavier
        document.addEventListener('keydown', function(event) {
            if (event.key === 'Escape') {
                closeImmigrationModal();
            }
        });

        // Fermer le modal en cliquant à l'extérieur
        document.getElementById('immigration-modal').addEventListener('click', function(e) {
            if (e.target === this) {
                closeImmigrationModal();
            }
        });

        // Animation d'apparition des slides
        slides.forEach((slide, index) => {
            slide.style.opacity = '0';
            slide.style.transform = 'translateY(30px)';
            
            setTimeout(() => {
                slide.style.transition = 'all 0.6s ease';
                slide.style.opacity = '1';
                slide.style.transform = 'translateY(0)';
            }, 200 * index);
        });
    });
